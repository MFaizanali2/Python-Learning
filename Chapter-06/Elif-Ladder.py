a = int(input("Enter Your Age: "))

# If Else Ladder

if a >= 18:
    print("You Are Eligible")
    print("Good For You")

elif a < 0:
    print("You Have Entered a Wrong Age")

elif a == 0:
    print("You Have Entered an Invalid Age")

else:
    print("You Are Not Eligible")

print("End of Program")
